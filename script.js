let currentLang = localStorage.getItem('lang') || 'en';

function updateLanguage() {
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        if (translations[currentLang][key]) {
            el.innerText = translations[currentLang][key];
        }
    });

    const langToggle = document.getElementById('lang-toggle');
    const langToggleMobile = document.getElementById('lang-toggle-mobile');
    const langText = currentLang === 'en' ? 'हिन्दी' : 'English';

    if (langToggle) langToggle.innerText = langText;
    if (langToggleMobile) langToggleMobile.innerText = langText;

    document.documentElement.lang = currentLang;
}

// --- Theme Logic ---
let currentTheme = localStorage.getItem('theme') || 'light';

function updateTheme() {
    document.documentElement.setAttribute('data-theme', currentTheme);
    const themeIcon = document.getElementById('theme-icon');
    const themeIconMobile = document.getElementById('theme-icon-mobile');

    if (currentTheme === 'dark') {
        if (themeIcon) themeIcon.className = 'fas fa-sun';
        if (themeIconMobile) themeIconMobile.className = 'fas fa-sun';
    } else {
        if (themeIcon) themeIcon.className = 'fas fa-moon';
        if (themeIconMobile) themeIconMobile.className = 'fas fa-moon';
    }
}

document.addEventListener('DOMContentLoaded', () => {
    updateLanguage();
    updateTheme();

    const langToggle = document.getElementById('lang-toggle');
    const langToggleMobile = document.getElementById('lang-toggle-mobile');

    const toggleLang = () => {
        currentLang = currentLang === 'en' ? 'hi' : 'en';
        localStorage.setItem('lang', currentLang);
        updateLanguage();
        // Trigger re-render of tools if tools.js is loaded and has a render function
        if (typeof renderFilteredTools === 'function') renderFilteredTools();
    };

    if (langToggle) langToggle.addEventListener('click', toggleLang);
    if (langToggleMobile) langToggleMobile.addEventListener('click', toggleLang);

    // Theme Toggle
    const themeToggle = document.getElementById('theme-toggle');
    const themeToggleMobile = document.getElementById('theme-toggle-mobile');

    const toggleTheme = () => {
        currentTheme = currentTheme === 'light' ? 'dark' : 'light';
        localStorage.setItem('theme', currentTheme);
        updateTheme();
    };

    if (themeToggle) themeToggle.addEventListener('click', toggleTheme);
    if (themeToggleMobile) themeToggleMobile.addEventListener('click', toggleTheme);

    // Mobile Menu
    const menuBtn = document.getElementById('menu-btn');
    const mobileMenu = document.getElementById('mobile-menu');
    if (menuBtn && mobileMenu) {
        menuBtn.addEventListener('click', () => {
            mobileMenu.classList.toggle('hidden');
            mobileMenu.classList.toggle('flex');
        });
    }

    // PWA Logic
    let deferredPrompt;
    const installBtns = document.querySelectorAll('.install-app-btn');

    window.addEventListener('beforeinstallprompt', (e) => {
        e.preventDefault();
        deferredPrompt = e;
        installBtns.forEach(btn => btn.classList.remove('hidden'));
    });

    installBtns.forEach(btn => {
        btn.addEventListener('click', async () => {
            if (deferredPrompt) {
                deferredPrompt.prompt();
                const { outcome } = await deferredPrompt.userChoice;
                if (outcome === 'accepted') {
                    installBtns.forEach(b => b.classList.add('hidden'));
                }
                deferredPrompt = null;
            }
        });
    });
});
